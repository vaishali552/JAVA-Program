package JavaPackage;

public class MultiplesTen {
	public static void main(String[] args) {
        System.out.println("Multiples of 10 between 10 and 1000:");

        // Loop through numbers from 10 to 1000
        for (int i = 10; i <= 1000; i += 10) {
            System.out.print(i + " ");
        }
    }
}
